#include "../fdf.h"

int	ft_rowsize(t_row *row)
{
	int		i;
	t_row	*p;

	p = row;
	i = 1;
	if (!row)
		return (0);
	while (p->next != NULL)
	{
		i++;
		p = p->next;
	}
	return (i);
}
